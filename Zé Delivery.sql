SELECT V.COD_VENDEDOR, V.NOME, P.COD_FORMA_PAGAMENTO, FP.DES_FORMA_PAGAMENTO 
FROM PEDIDO p  
INNER JOIN VENDEDOR V ON V.COD_VENDEDOR = P.COD_VENDEDOR
INNER JOIN FORMA_PAGAMENTO FP ON FP.COD_FORMA_PAGAMENTO = P.COD_FORMA_PAGAMENTO


-- INNER JOIN
-- 2 - QUERO RECUPERAR TODAS AS SITUAÇÕES DOS PEDIDOS
SELECT C.COD_CLIENTE,C.NOME AS NOME_CLIENTE, SP.STATUS_PEDIDO
FROM CLIENTE C
INNER JOIN PEDIDO pd ON C.COD_CLIENTE = Pd.COD_CLIENTE
INNER JOIN SITUACAO_PEDIDO SP ON SP.COD_SITUACAO_PEDIDO = pd.COD_SITUACAO_PEDIDO


-- INNER JOIN
-- 3 - QUERO RECUPERAR TODOS OS LOGRADOURO DOS FORNECEDORES
SELECT F.NOME, F.COD_LOGRADOURO, L."RUA/AV" , L.BAIRRO, L.CEP  
FROM LOGRADOURO l  
INNER JOIN FORNECEDOR f  ON F.COD_LOGRADOURO  =  L.COD_LOGRADOURO 


-- INNER JOIN
-- 4 - QUERO RECUPERAR OS COD_VENDEDOR E ADMIM
SELECT P.COD_VENDEDOR, V.NOME AS NOME_VENDEDOR, V.ADMIM 
FROM PEDIDO p 
INNER JOIN VENDEDOR V ON V.COD_VENDEDOR = P.COD_VENDEDOR

-- INNER JOIN
-- 5 - QUERO SABER O VALOR TOTAL, NOME, VALOR DO PRODUTO 
SELECT R.COD_RELATORIO, R.COD_PRODUTO, R.VLR_TOTAL, P.NOME AS NOME_PRODUTO, 
P.VLR_PRODUTO 
FROM RELATORIO r 
INNER JOIN PRODUTO P ON P.COD_PRODUTO = R.COD_PRODUTO


-- LEFT JOIN
-- 1 - TODOS PEDIDOS FEITO PELOS CLIENTES 
SELECT P.VLR_PRODUTO, P.NOME, C.NOME AS NOME_CLIENTE
FROM PRODUTO p 
LEFT JOIN CLIENTE c ON C.COD_CLIENTE = P.COD_CLIENTE
INNER JOIN P

-- RIGHT JOIN
-- 2 - TODOS OS FORNECEDORES DO ZÉ DELIVERY
SELECT F.NOME, F.TELEFONE, F.CNPJ  
FROM FORNECEDOR F 
RIGHT JOIN PEDIDO P ON F.COD_FORNECEDOR = P.COD_FORNECEDOR 

--FULL OUTER JOIN
-- 3 - QUERO BUSCAR TODOS OS FORNECEDORES CADASTRADOS COM SEUS LOGRADOUROS
